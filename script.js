function searchRecipe(event) {
  event.preventDefault();
  const query = document.getElementById("searchInput").value.trim().toLowerCase();
  const resultsDiv = document.getElementById("results");

  if (!query) {
    resultsDiv.innerHTML = "<p>Please enter a search term.</p>";
    return;
  }

  const filtered = rcipe.filter(recipe => recipe.toLowerCase().startsWith(query));

  if (filtered.length > 0) {
    resultsDiv.innerHTML = `<p>Results:</p><ul>${filtered.map(r => `<li>${r}</li>`).join('')}</ul>`;
  } else {
    resultsDiv.innerHTML = "<p>No recipes found starting with that letter.</p>";
  }
}

